public class arithmetic {
 
 public static void main(String[] args) {  
 
  //variable declaration
  int num1 = 5;
  int num2 = 10;
 
  //displaying the output
  System.out.println(num1 + num2);
   
  System.out.println(num1 - num2);
   
  System.out.println(num1 * num2);
   
  System.out.println(num1 / num2);
 
  System.out.println(num1 % num2);
 }
 
}
